package com.kh.chap01.abstraction.run;

import java.util.Scanner;

import com.kh.chap01.abstraction.vo.Puppy;

public class PuppyCareRun {

	public static void main(String[] args) {
		// 사용자로부터 강아지의 정보를 입력받아서 그 정보를 출력해주고 강아지와 함께 놀아줄 수 있는 프로그램
		
		System.out.println("귀여운 개 키우기");
		Scanner sc = new Scanner(System.in); // 좌항 - 스캐너 타입의 변수 선언. 우항 - 스캐너 객체 생성
		Puppy puppy = new Puppy();
		System.out.print("개의 이름을 입력해주세요. > ");
		puppy.name = sc.nextLine();
		System.out.print("개의 품종을 입력해주세요. > ");
		puppy.species = sc.nextLine();
		System.out.print("개의 나이를 입력해주세요. > ");
		puppy.age = sc.nextInt();
		sc.nextLine(); // 빼먹지 말 것!
		System.out.print("개의 색상을 입력해주세요. > ");
		puppy.color = sc.nextLine();
		System.out.print("개의 성별을 입력해주세요. > ");
		puppy.gender = sc.nextLine();
		System.out.print("개의 체중을 입력해주세요. > ");
		puppy.weight = sc.nextInt();
		sc.nextLine(); // 빼먹지 말 것!
		
		boolean exit = false;
		while(!exit) {
			System.out.println("==========");
			System.out.println(puppy.name + "의 정보!");
			System.out.println("나이 : " + puppy.age);
			System.out.println("품종 : " + puppy.species);
			System.out.println("색상 : " + puppy.color);
			System.out.println("성별 : " + puppy.gender);
			System.out.println("체중 : " + puppy.weight);
			System.out.println("==========");
			System.out.println();
			
			System.out.println("메뉴를 선택해주세요 > ");
			if (puppy.weight > 40) {
				System.out.println("다이어트를 위해 짖기를 추천드려요");
			}
			System.out.print("1. 냄새맡기 / 2. 짖기 / 3. 프로그램 종료 > ") ;
			int menuNo = sc.nextInt();
			sc.nextLine();
			
			switch(menuNo) {
			case 1 : puppy.sniff(); break;
			case 2 : puppy.bark(); break;
			case 3 : exit = true; System.out.print("프로그램을 종료합니다."); break;
			default : System.out.println("메뉴를 다시 선택해주세요.");
			}
			
		}
		
		
		
		
		
	}

}
