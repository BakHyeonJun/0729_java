package com.kh.chap01.abstraction.vo;

/*
 * 이제부터 익힐 class 형태는 이하와 같음
 * public class 클래스식별자 {
 * 
 * 		필드부(속성 - 값을 저장하는 공간으로 이해하면 됨 - 을 의미)
 * 
 * 		생성자부
 * 
 * 		메소드부
 * 
 * 
 * }
 * 
 */

public class Puppy {
	
	// 필드부 표현형 - 접근제한자 자료형 필드식별자;
	// 접근제한자 : 이 필드에 접근할 수 있는 접근권한을 제어하는 역할
	// public > protected > default > private
	// 클래스 내부에 필드를 선언할 때는 반드시 접근제한자를 붙여야 함.
	
	// 필드부
	public String species;
	public String name;
	public int age;
	public String gender;
	public String color;
	public int weight;
	
	// 생성자부
	
	
	
	
	
	// 메소드부
	public void sniff() {
		System.out.println(name + "가 킁킁거리며 냄새를 맡습니다.");
	}
	public void bark() {
		if(weight > 15) {
		System.out.println(name + "가 멍하고 짖습니다.");
		weight = weight - 5;
		} else {
			System.out.println("무리한 다이어트는 불가합니다.");
		}
	}
	

}
